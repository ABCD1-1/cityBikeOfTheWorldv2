const mongoose = require("mongoose");

const networkSchema = new mongoose.Schema({
  id: {
    type: String,
    required: true,
    unique: true,
  },
  name: {
    type: String,
    required: true,
  },
  company: {
    type: Array,
    required: true,
  },
  city: {
    type: String,
    required: true,
  },
  country: {
    type: String,
    required: true,
  },
});

const NetworkModel = mongoose.model("network", networkSchema);
module.exports = NetworkModel;
